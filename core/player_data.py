def get_pre_match_quote(player_name: str, opponent_name: str | None = None) -> str:
    """경기 전 대사 반환. 라이벌이면 라이벌 전용 대사 우선."""
    data = PLAYER_DATA.get(player_name)
    if not data:
        return ""
    rival = data.get("rival", "")
    if opponent_name and opponent_name == rival:
        return data.get("rival_quote", "")
    return random.choice(data.get("pre_match", [""]))


def get_win_quote(player_name: str) -> str:
    data = PLAYER_DATA.get(player_name)
    if not data:
        return ""
    return random.choice(data.get("win", [""]))


def get_loss_quote(player_name: str) -> str:
    data = PLAYER_DATA.get(player_name)
    if not data:
        return ""
    return random.choice(data.get("loss", [""]))


def get_style(player_name: str) -> str:
    return PLAYER_DATA.get(player_name, {}).get("style", "")
