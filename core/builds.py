# 가위바위보 승리 관계: {이기는_빌드: 지는_빌드}
RPS_WINS = {"가위": "보", "보": "바위", "바위": "가위"}

# 빌드 우위 시 초반 파워 부스트
# PRD v10: 15→10 (전략 보너스와 중복 시 너무 강했음)
BUILD_ADVANTAGE_BOOST = 10

BUILD_TYPES = ["가위", "바위", "보"]


def get_build_name(my_race: str, opp_race: str, build_type: str) -> str:
    """종족 매치업에 맞는 빌드 이름 반환. 없으면 빌드 타입명 반환."""
    return BUILD_NAMES.get(my_race, {}).get(opp_race, {}).get(build_type, build_type)


def calc_build_result(build_a: str, build_b: str) -> int:
    """빌드 우열 계산.

    Returns:
        +1: a 승리 (a가 b를 이김)
         0: 무승부
        -1: b 승리 (b가 a를 이김)
    """
    if build_a == build_b:
        return 0
    if RPS_WINS.get(build_a) == build_b:
        return 1
    return -1
